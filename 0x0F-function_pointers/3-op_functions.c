#include "3-calc.h"
#include <stdlib.h>
/**
 * op_add - returns the addition of two integers
 * @a: first operand
 * @b: second operand
 * Returns: the addition of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the soustraction of two integers
 * @a: first operand
 * @b: second operand
 * Returns: the soustraction of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the mul of two integers
 * @a: first operand
 * @b: second operand
 * Returns: the mul of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the div of two integers
 * @a: first operand
 * @b: second operand
 * Returns: the div of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the addition of two integers
 * @a: first operand
 * @b: second operand
 * Returns: the modulo of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
